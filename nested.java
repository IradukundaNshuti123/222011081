//Java program to illustrate the nested for
class kkk {
public static void main(String[] args) {
// Outer loop
    for (int i=1;i<5;i++){
        System.out.println("outside : i="+i);
        // Inner loop
        for (int j=1;j<=3;j++){
            System.out.println("Inside: j="+j);
        }
    }
}
}
